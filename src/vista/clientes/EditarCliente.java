    private void Cl_EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Cl_EditarActionPerformed
        // Obtener datos del formulario
        String docIdentidad = Cl_DocumentoIdentidad.getText().trim();
        String nombre = CL_Nombre.getText().trim();
        String apellido = Cl_Apellido.getText().trim();
        String direccion = Cl_Direccion.getText().trim();
        String telefono = Cl_Telefono.getText().trim();
        String correo = Cl_Correo.getText().trim();

        // Validar datos
        if (docIdentidad.isEmpty() || nombre.isEmpty() || apellido.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Los campos Documento, Nombre y Apellido son obligatorios.");
            return;
        }

        // Validar formato de correo
        String regex = "^[\\w.-]+@[\\w.-]+\\.[a-zA-Z]{2,}$";
        if (!correo.matches(regex)) {
            JOptionPane.showMessageDialog(this, "⚠️ El correo no tiene un formato válido.", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Crear un cliente con los datos editados
        Cliente cliente = new Cliente();
        cliente.setIdCliente(seleccionado.getIdCliente());
        cliente.setIdDocIdentidad(docIdentidad);
        cliente.setNombre(nombre);
        cliente.setApellido(apellido);
        cliente.setDireccion(direccion);
        cliente.setTelefono(telefono);
        cliente.setCorreo(correo);
        cliente.setFechaRegistro(seleccionado.getFechaRegistro()); // Mantener la fecha original

        // Usar el controlador para actualizar
        try {
            ClienteControlador controlador = new ClienteControlador();
            if (controlador.modificarCliente(cliente)) {
                JOptionPane.showMessageDialog(this, "✅ Cliente actualizado correctamente.");
                // Actualizar los datos en el formulario para reflejar los cambios
                seleccionado = cliente; // Actualizar el cliente seleccionado
                setDatosCliente(cliente); // Refrescar los campos del formulario
            } else {
                JOptionPane.showMessageDialog(this, "❌ No se pudo actualizar el cliente.");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "❌ Error al actualizar el cliente: " + ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_Cl_EditarActionPerformed

